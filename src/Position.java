public class Position {
    int c,r;

    public Position(int r, int c){
        this.c = c;
        this.r = r;
    }

    public int getC() {
        return c;
    }

    public int getR() {
        return r;
    }

    public void setC(int c) {
        this.c = c;
    }

    public void setR(int r) {
        this.r = r;
    }
}
